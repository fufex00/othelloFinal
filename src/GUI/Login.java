    /**
     * método que contiene el botón de registrar
     * @param evt evento
     */
    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        Players player = new Players(); //NO BORRAR!

        System.out.println("Entra al login");
        try {
            name = JOptionPane.showInputDialog("Escriba su nombre");
            if (name.isEmpty()) {
                JOptionPane.showMessageDialog(null, "Digite un texto válido");
            } else {
                player.setName(name);
                data.playersList.add(player);
                System.out.println("tamano lista " + data.playersList.size());
                for (int i = 0; i < data.playersList.size(); i++) {
                    System.out.println("Y tiene los elementos: " + data.playersList.get(i).getName());
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "No hay datos para registrar");
        }
    }//GEN-LAST:event_btnLoginActionPerformed

    /**
     * método que contiene el botón de un jugador
     * @param evt evento
     */
    private void btnOnePlayerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOnePlayerActionPerformed

        try {
            if (data.playersList.isEmpty()) {
                JOptionPane.showMessageDialog(null, "La lista esta vacia, por favor registrese");
            } else {
                String name = JOptionPane.showInputDialog("Escriba su nombre");
                if (name.isEmpty()) {
                    JOptionPane.showMessageDialog(null, "Digite un texto válido");
                } else {
                    for (int i = 0; i < data.playersList.size() + 1; i++) {
                        if (data.playersList.get(i).getName().equals(name)) {

                            System.out.println("Accediendo...");
                            JFOthelloPrincipal panel = new JFOthelloPrincipal();

                            panel.setVisible(true);
                            panel.lblPlayerOne.setText(name);
                            panel.lblPlayertwo.setText("");
                            panel.lblWhitePieces.setText("");
                            this.setVisible(false);
                        }
                    }
                }
            }
        } catch (Exception e) {
        }
    }//GEN-LAST:event_btnOnePlayerActionPerformed
    
