    /**
     * método que contiene el botón de dos jugadores
     * @param evt evento
     */
    private void btnTwoPlayersActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTwoPlayersActionPerformed
        String aux = "";
        try {
            if (data.playersList.isEmpty() || data.playersList.size() < 2) {
                JOptionPane.showMessageDialog(null, "No hay jugadores suficientes para acceder a este modo");
            } else {
                String name1 = JOptionPane.showInputDialog("Escriba el nombre del primer jugador");
                if (name1.isEmpty()) {
                    JOptionPane.showMessageDialog(null, "Elementos vacios, digite texto");
                } else {
                    for (int i = 0; i < data.playersList.size(); i++) {

                        System.out.println(data.playersList.get(i).getName());
                        if (data.playersList.get(i).getName().equals(name1)) {
                            name2 = JOptionPane.showInputDialog("Escriba el nombre del segundo jugador");
                            for (int j = 0; j < data.playersList.size(); j++) {
                                if (data.playersList.get(j).getName().equals(name2)) {
                                    JFOthelloPrincipal panel = new JFOthelloPrincipal();
                                    panel.setVisible(true);
                                    panel.lblPlayerOne.setText(name1);
                                    panel.lblPlayertwo.setText(name2);
                                    this.setVisible(false);
                                }
                            }
                        }
                    }
                }
            }
        } catch (HeadlessException headlessException) {
        }
    }//GEN-LAST:event_btnTwoPlayersActionPerformed

